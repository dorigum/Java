package ch02.sec01;


// 변수 선언
// 변수에 값 저장
// 변수에 저장되어 있는 값 출력
public class VariableExample {
	
	public static void main(String[] args) {
		// 10을 value 변수에 초기값으로 해서 변수 선언
		int value = 10 ;
		
		System.out.println(value);
		
		// 변수 value 값을 읽어서 10을 더해서
		// 변수 result에 저장
		
		int result = value + 10 ;
		
		// 변수 result 값 출력
		System.out.println(result);
		
	}

}
